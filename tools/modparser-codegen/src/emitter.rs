/// Generate the complete mod_parser_generated.rs content.
pub fn emit_generated(data: &ParsedModParser) -> Result<String, String> {
    let mut out = String::with_capacity(512 * 1024);

    emit_header(&mut out);
    emit_form_type_enum(&mut out);
    emit_form_patterns(&mut out, &data.forms)?;
    emit_mod_name_map(&mut out, &data.mod_names);
    emit_mod_flag_map(&mut out, &data.mod_flags);
    emit_pre_flag_patterns(&mut out, &data.pre_flags)?;
    emit_mod_tag_patterns(&mut out, &data.mod_tags)?;
    emit_suffix_types(&mut out);
    emit_pen_types(&mut out);
    emit_cost_types(&mut out);
    emit_base_cost_types(&mut out);
    emit_regen_types(&mut out);
    emit_degen_types(&mut out);
    emit_dmg_types(&mut out);
    emit_flag_types(&mut out);
    emit_special_mods(&mut out, &data.special_mods)?;
    emit_scan_functions(&mut out);
    emit_normalize_fn(&mut out);
    emit_pipeline_fn(&mut out);
    emit_count_constants(&mut out, &data.special_mods);

    Ok(out)
}

/// Generate the mod_parser_manual_manifest.rs content (stubs for manual handlers).
pub fn emit_manual_manifest(data: &ParsedModParser) -> Result<String, String> {
    let mut out = String::with_capacity(16 * 1024);

    out.push_str(
        "//! Auto-generated manifest of specialModList entries requiring manual handlers.\n",
    );
    out.push_str("//!\n");
    out.push_str(
        "//! Each entry lists: handler ID, regex pattern, capture count, Lua source line.\n",
    );
    out.push_str("//! Implement handlers in mod_parser_manual.rs.\n\n");

    let manual_entries: Vec<(usize, &SpecialModEntry)> = data
        .special_mods
        .iter()
        .enumerate()
        .filter(|(_, e)| matches!(e.template, SpecialModTemplate::ManualRequired { .. }))
        .collect();

    out.push_str(&format!(
        "/// Number of entries requiring manual handlers.\n\
         pub const MANUAL_ENTRY_COUNT: usize = {};\n\n",
        manual_entries.len()
    ));

    out.push_str(
        "/// Manual handler stubs.\n\
         /// Format: (handler_id, regex_pattern, capture_count, lua_line)\n\
         pub const MANUAL_ENTRIES: &[(&str, &str, usize, usize)] = &[\n",
    );

    for (idx, entry) in &manual_entries {
        let handler_id = format!("manual_{}", idx);
        let (regex_str, capture_count) =
            lua_pattern_to_regex(&entry.pattern.0).unwrap_or_else(|_| (entry.pattern.0.clone(), 0));
        let escaped = escape_rust_string(&regex_str);
        let lua_line = entry.line_number;
        if let SpecialModTemplate::ManualRequired { ref lua_body, .. } = entry.template {
            let body_preview: String = lua_body.chars().take(120).collect();
            let body_escaped = escape_rust_string(&body_preview);
            out.push_str(&format!(
                "    // Lua line {}: {}\n    (\"{}\", r#\"{}\"#, {}, {}),\n",
                lua_line, body_escaped, handler_id, escaped, capture_count, lua_line
            ));
        }
    }

    out.push_str("];\n");

    Ok(out)
}

