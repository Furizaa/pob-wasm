fn emit_mod_name_map(out: &mut String, mod_names: &[ModNameEntry]) {
    out.push_str(
        "/// Entry in the mod name lookup.\n\
         pub struct StaticModNameEntry {\n\
         \x20   pub names: Vec<&'static str>,\n\
         \x20   pub tags: Vec<StaticTag>,\n\
         }\n\n",
    );

    out.push_str(&format!(
        "/// Mod name lookup ({} entries).\n",
        mod_names.len()
    ));
    out.push_str(
        "pub static MOD_NAME_MAP: Lazy<HashMap<&'static str, StaticModNameEntry>> = Lazy::new(|| {\n\
         \x20   let mut m = HashMap::new();\n",
    );

    for entry in mod_names {
        let key = escape_rust_string(&entry.key);
        let names: Vec<String> = entry
            .names
            .iter()
            .map(|n| format!("\"{}\"", escape_rust_string(n)))
            .collect();
        let names_str = names.join(", ");
        let tags_str = emit_static_tags(&entry.tags);
        out.push_str(&format!(
            "    m.insert(\"{}\", StaticModNameEntry {{ names: vec![{}], tags: vec![{}] }});\n",
            key, names_str, tags_str
        ));
    }

    out.push_str("    m\n});\n\n");
}

