// FORM_PATTERNS
// ────────────────────────────────────────────────────────────────────────────

fn emit_form_patterns(out: &mut String, forms: &[FormEntry]) -> Result<(), String> {
    out.push_str(&format!(
        "/// Form patterns from formList ({} entries).\n",
        forms.len()
    ));
    out.push_str(
        "pub static FORM_PATTERNS: Lazy<Vec<(Lazy<Regex>, FormType)>> = Lazy::new(|| vec![\n",
    );

    for entry in forms {
        let (regex_str, _caps) = lua_pattern_to_regex(&entry.pattern.0)
            .map_err(|e| format!("form pattern '{}': {}", entry.pattern.0, e))?;
        let form_variant = form_type_to_rust(&entry.form);
        let escaped = escape_rust_string(&regex_str);
        out.push_str(&format!(
            "    (Lazy::new(|| Regex::new(r#\"{}\"#).unwrap()), FormType::{}),\n",
            escaped, form_variant
        ));
    }

    out.push_str("]);\n\n");
    Ok(())
}

