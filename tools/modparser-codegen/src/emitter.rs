/// Scan form patterns and extract form type + numeric value + extra capture.
fn scan_form_patterns(line: &str) -> Option<(FormType, f64, Option<String>)> {
    if let Some((idx, caps, _start, _end)) = scan_regex(line, &*FORM_PATTERNS) {
        let form = &FORM_PATTERNS[idx].1;
        let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or("0");
        let num: f64 = num_str.parse().unwrap_or(0.0);
        let extra = caps.get(2).map(|m| m.as_str().to_string());
        Some((form.clone(), num, extra))
    } else {
        None
    }
}

/// Scan pre-flag patterns (from the Lazy<Vec<...>>).
fn scan_regex_for_pre_flags<'a>(line: &'a str, patterns: &[(Lazy<Regex>, StaticPreFlagEntry)]) -> Option<(usize, regex::Captures<'a>, usize, usize)> {
    let mut best: Option<(usize, usize, usize, usize, usize)> = None;

    for (i, (re, _)) in patterns.iter().enumerate() {
        if let Some(m) = re.find(line) {
            let start = m.start();
            let end = m.end();
            let match_len = end - start;
            let pat_len = re.as_str().len();

            let is_better = match best {
                None => true,
                Some((_, bs, _be, bml, bpl)) => {
                    start < bs
                        || (start == bs && match_len > bml)
                        || (start == bs && match_len == bml && pat_len > bpl)
                }
            };

            if is_better {
                best = Some((i, start, end, match_len, pat_len));
            }
        }
    }

    if let Some((idx, start, end, _, _)) = best {
        let re = &patterns[idx].0;
        let caps = re.captures(line)?;
        Some((idx, caps, start, end))
    } else {
        None
    }
}

