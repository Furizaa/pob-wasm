fn parse_mod_flags_expr(s: &str) -> String {
    let trimmed = s.trim();
    if trimmed == "0" || trimmed.is_empty() {
        return "ModFlags::NONE".to_string();
    }

    // Handle bor(...) or ModFlag.X patterns
    let cleaned = trimmed.replace("bor(", "").replace(')', "");
    let parts: Vec<&str> = cleaned
        .split(',')
        .map(|s| s.trim())
        .filter(|s| !s.is_empty())
        .collect();

    let mut exprs = Vec::new();
    for part in &parts {
        // Also handle `|` separated
        for sub in part.split('|') {
            let sub = sub.trim();
            if sub == "0" || sub.is_empty() {
                continue;
            }
            if let Some(flag) = translate_mod_flag(sub) {
                exprs.push(flag);
            }
        }
    }

    if exprs.is_empty() {
        "ModFlags::NONE".to_string()
    } else if exprs.len() == 1 {
        exprs[0].clone()
    } else {
        // Use BitOr: ModFlags::X | ModFlags::Y — this returns ModFlags
        exprs.join(" | ")
    }
}

