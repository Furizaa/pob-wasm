fn emit_special_mods(out: &mut String, special_mods: &[SpecialModEntry]) -> Result<(), String> {
    out.push_str(&format!(
        "/// Special mod patterns ({} entries).\n",
        special_mods.len()
    ));
    out.push_str("pub static SPECIAL_MOD_PATTERNS: Lazy<Vec<Lazy<Regex>>> = Lazy::new(|| vec![\n");

    for entry in special_mods {
        let (regex_str, _) = lua_pattern_to_regex(&entry.pattern.0)
            .map_err(|e| format!("special_mod '{}': {}", entry.pattern.0, e))?;
        let escaped = escape_rust_string(&regex_str);
        out.push_str(&format!(
            "    Lazy::new(|| Regex::new(r#\"{}\"#).unwrap()),\n",
            escaped
        ));
    }

    out.push_str("]);\n\n");

    // Now emit the handler function
    emit_special_mod_handler(out, special_mods)?;

    Ok(())
}

fn emit_special_mod_handler(
    out: &mut String,
    special_mods: &[SpecialModEntry],
) -> Result<(), String> {
    out.push_str(
        "/// Handle a matched special mod by index.\n\
         /// Returns Some(Vec<Mod>) if handled, None if manual handler needed.\n\
         fn handle_special_mod(\n\
         \x20   idx: usize,\n\
         \x20   caps: &regex::Captures,\n\
         \x20   source: &ModSource,\n\
         \x20   manual_handler: &dyn Fn(&str, &regex::Captures, &ModSource) -> Vec<Mod>,\n\
         ) -> Vec<Mod> {\n\
         \x20   match idx {\n",
    );

    for (i, entry) in special_mods.iter().enumerate() {
        out.push_str(&format!("        {} => {{\n", i));
        emit_template_handler(out, &entry.template, i)?;
        out.push_str("        }\n");
    }

    out.push_str(
        "        _ => vec![],\n\
         \x20   }\n\
         }\n\n",
    );

    Ok(())
}

fn emit_template_handler(
    out: &mut String,
    template: &SpecialModTemplate,
    idx: usize,
) -> Result<(), String> {
    match template {
        SpecialModTemplate::StaticMods(calls) => {
            if calls.is_empty() {
                out.push_str("            vec![]\n");
            } else {
                out.push_str("            vec![\n");
                for call in calls {
                    emit_mod_call_construction(out, call, "source");
                }
                out.push_str("            ]\n");
            }
        }
        SpecialModTemplate::SimpleFn(calls) => {
            // Extract the first capture as num
            out.push_str(
                "            let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or(\"0\");\n",
            );
            out.push_str("            let num: f64 = num_str.parse().unwrap_or(0.0);\n");
            if calls.is_empty() {
                out.push_str("            vec![]\n");
            } else {
                out.push_str("            vec![\n");
                for call in calls {
                    emit_mod_call_construction(out, call, "source");
                }
                out.push_str("            ]\n");
            }
        }
        SpecialModTemplate::HelperCall { helper, args } => {
            // Generate a call to a helper stub
            let handler_id = format!("helper_{}_{}", helper, idx);
            out.push_str(&format!(
                "            // Helper: {}({})\n",
                helper,
                args.join(", ")
            ));
            out.push_str(&format!(
                "            manual_handler(\"{}\", caps, source)\n",
                handler_id
            ));
        }
        SpecialModTemplate::DamageConversion { stat_prefix, .. } => {
            out.push_str(
                "            let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or(\"0\");\n",
            );
            out.push_str("            let num: f64 = num_str.parse().unwrap_or(0.0);\n");
            out.push_str(
                "            let dmg_type = caps.get(2).map(|m| m.as_str()).unwrap_or(\"\");\n",
            );
            out.push_str("            let type_cap = capitalize_first(dmg_type);\n");
            let prefix = escape_rust_string(stat_prefix);
            out.push_str(&format!(
                "            let stat_name = format!(\"{{}}ConvertTo{{}}\", \"{}\", type_cap);\n",
                prefix
            ));
            out.push_str("            vec![Mod {\n");
            out.push_str("                name: stat_name,\n");
            out.push_str("                mod_type: ModType::Base,\n");
            out.push_str("                value: ModValue::Number(num),\n");
            out.push_str("                flags: ModFlags::NONE,\n");
            out.push_str("                keyword_flags: KeywordFlags::NONE,\n");
            out.push_str("                tags: vec![],\n");
            out.push_str("                source: source.clone(),\n");
            out.push_str("            }]\n");
        }
        SpecialModTemplate::DamageGainAs { stat_prefix, .. } => {
            out.push_str(
                "            let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or(\"0\");\n",
            );
            out.push_str("            let num: f64 = num_str.parse().unwrap_or(0.0);\n");
            out.push_str(
                "            let dmg_type = caps.get(2).map(|m| m.as_str()).unwrap_or(\"\");\n",
            );
            out.push_str("            let type_cap = capitalize_first(dmg_type);\n");
            let prefix = escape_rust_string(stat_prefix);
            out.push_str(&format!(
                "            let stat_name = format!(\"{{}}GainAs{{}}\", \"{}\", type_cap);\n",
                prefix
            ));
            out.push_str("            vec![Mod {\n");
            out.push_str("                name: stat_name,\n");
            out.push_str("                mod_type: ModType::Base,\n");
            out.push_str("                value: ModValue::Number(num),\n");
            out.push_str("                flags: ModFlags::NONE,\n");
            out.push_str("                keyword_flags: KeywordFlags::NONE,\n");
            out.push_str("                tags: vec![],\n");
            out.push_str("                source: source.clone(),\n");
            out.push_str("            }]\n");
        }
        SpecialModTemplate::NumericScaling { mod_call, factor } => {
            out.push_str(
                "            let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or(\"0\");\n",
            );
            out.push_str("            let num: f64 = num_str.parse().unwrap_or(0.0);\n");
            out.push_str(&format!("            let scaled = num * {:.6};\n", factor));
            emit_single_mod_call_with_value(out, mod_call, "scaled", "source");
        }
        SpecialModTemplate::EnemyModifier(calls) => {
            if calls.is_empty() {
                out.push_str("            vec![]\n");
            } else {
                // For EnemyModifier, we wrap the inner mod in an EnemyModifier LIST mod
                out.push_str(
                    "            let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or(\"0\");\n",
                );
                out.push_str("            let num: f64 = num_str.parse().unwrap_or(0.0);\n");
                out.push_str("            // EnemyModifier wrapper — simplified\n");
                out.push_str("            vec![\n");
                for call in calls {
                    emit_mod_call_construction(out, call, "source");
                }
                out.push_str("            ]\n");
            }
        }
        SpecialModTemplate::MinionModifier(calls) => {
            if calls.is_empty() {
                out.push_str("            vec![]\n");
            } else {
                out.push_str(
                    "            let num_str = caps.get(1).map(|m| m.as_str()).unwrap_or(\"0\");\n",
                );
                out.push_str("            let num: f64 = num_str.parse().unwrap_or(0.0);\n");
                out.push_str("            // MinionModifier wrapper — simplified\n");
                out.push_str("            vec![\n");
                for call in calls {
                    emit_mod_call_construction(out, call, "source");
                }
                out.push_str("            ]\n");
            }
        }
        SpecialModTemplate::ManualRequired {
            lua_body,
            line_number,
        } => {
            let handler_id = format!("manual_{}", idx);
            let body_preview: String = lua_body.chars().take(80).collect();
            let comment = escape_rust_string(&body_preview);
            out.push_str(&format!(
                "            // Manual: Lua line {} — {}\n",
                line_number, comment
            ));
            out.push_str(&format!(
                "            manual_handler(\"{}\", caps, source)\n",
                handler_id
            ));
        }
    }
    Ok(())
}

