/// Emit a resource type lookup with the given suffix.
fn emit_resource_suffixed_map(out: &mut String, name: &str, suffix: &str, doc: &str) {
    out.push_str(&format!(
        "/// {}\n\
         pub static {}: Lazy<HashMap<&'static str, Vec<String>>> = Lazy::new(|| {{\n\
         \x20   let mut m: HashMap<&'static str, Vec<String>> = HashMap::new();\n",
        doc, name
    ));

    // resourceTypes from ModParser.lua
    let resources: Vec<(&str, Vec<&str>)> = vec![
        ("life", vec!["Life"]),
        ("mana", vec!["Mana"]),
        ("energy shield", vec!["EnergyShield"]),
        ("life and mana", vec!["Life", "Mana"]),
        ("life and energy shield", vec!["Life", "EnergyShield"]),
        (
            "life, mana and energy shield",
            vec!["Life", "Mana", "EnergyShield"],
        ),
        (
            "life, energy shield and mana",
            vec!["Life", "Mana", "EnergyShield"],
        ),
        ("mana and life", vec!["Life", "Mana"]),
        ("mana and energy shield", vec!["Mana", "EnergyShield"]),
        (
            "mana, life and energy shield",
            vec!["Life", "Mana", "EnergyShield"],
        ),
        (
            "mana, energy shield and life",
            vec!["Life", "Mana", "EnergyShield"],
        ),
        ("energy shield and life", vec!["Life", "EnergyShield"]),
        ("energy shield and mana", vec!["Mana", "EnergyShield"]),
        (
            "energy shield, life and mana",
            vec!["Life", "Mana", "EnergyShield"],
        ),
        (
            "energy shield, mana and life",
            vec!["Life", "Mana", "EnergyShield"],
        ),
        ("rage", vec!["Rage"]),
    ];

    // Also include "maximum X" variants
    let mut all_resources = resources.clone();
    for (key, vals) in &resources {
        all_resources.push((&"", vals.clone())); // placeholder, replaced below
    }
    // Actually, build it properly:
    out.push_str("    // Base resource types\n");
    for (key, vals) in &resources {
        let suffixed: Vec<String> = vals.iter().map(|v| format!("{}{}", v, suffix)).collect();
        let vals_str = suffixed
            .iter()
            .map(|s| format!("\"{}\".to_string()", s))
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!("    m.insert(\"{}\", vec![{}]);\n", key, vals_str));
    }

    out.push_str("    // Maximum resource types\n");
    for (key, vals) in &resources {
        let max_key = format!("maximum {}", key);
        let suffixed: Vec<String> = vals.iter().map(|v| format!("{}{}", v, suffix)).collect();
        let vals_str = suffixed
            .iter()
            .map(|s| format!("\"{}\".to_string()", s))
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!(
            "    m.insert(\"{}\", vec![{}]);\n",
            max_key, vals_str
        ));
    }

    out.push_str("    m\n});\n\n");
}

