fn lua_value_to_rust(value: &str, mod_type: &str) -> String {
    let trimmed = value.trim();

    // Boolean
    if trimmed == "true" {
        return "ModValue::Bool(true)".to_string();
    }
    if trimmed == "false" {
        return "ModValue::Bool(false)".to_string();
    }

    // String value (quoted)
    if trimmed.starts_with('"') && trimmed.ends_with('"') {
        let inner = &trimmed[1..trimmed.len() - 1];
        return format!(
            "ModValue::String(\"{}\".to_string())",
            escape_rust_string(inner)
        );
    }

    // Variable references from captures (e.g. "num", "cap1")
    if trimmed == "num" || trimmed == "cap1" {
        return "ModValue::Number(num)".to_string();
    }
    if trimmed == "-num" {
        return "ModValue::Number(-num)".to_string();
    }

    // Numeric literal
    if let Ok(n) = trimmed.parse::<f64>() {
        return format!("ModValue::Number({:.1})", n);
    }

    // Complex table/expression — default to num
    if trimmed.contains("tonumber") || trimmed.contains("cap") {
        return "ModValue::Number(num)".to_string();
    }

    // Fallback
    format!(
        "ModValue::Number(0.0) /* TODO: {} */",
        escape_rust_string(trimmed)
    )
}

